public class Calcular {
    public static double soma (double numero01, double numero02){
        return numero01 + numero02;
    }
    public static double sub (double numero01, double numero02){
        return numero01 - numero02;
    }
    public static double mult(double numero01, double numero02){
        return numero01 * numero02;
    }
    public static double div (double numero01, double numero02){
        return numero01/numero02;
    }
}
